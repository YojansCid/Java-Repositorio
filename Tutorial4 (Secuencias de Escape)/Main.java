public class Main {

    public static void main(String[] args) {
        //Secuencias de Escape en los textos
        System.out.println("Hola \nMundo salto de linea"); // "\n" sirve para saltar una linea en el texto
        System.out.println("Hola \tMundo tabulador"); // "\t" sirve para hacer una tabulacion
        System.out.println("Hola \"Mundo comillas\""); // "\"" sirve para agregar comillas a los textos
        System.out.println("Hola \\ Mundo backSlash"); // "\"" sirve para mostrar el "\"
    }
    
}
